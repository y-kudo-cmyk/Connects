// P_JP002 "We Make You" — cleanup & version_id rename (2026-04)
//
// User directive (update to 01-we-make-you.mjs):
//   - 限定C "×" は「限定C は P_JP002 に存在しない」= 削除 の意味だった。
//     既に card_versions に V_JP001_03 (限定C) があるので削除する。
//   - ついでに既存の ID バグ (V_JP001_* が P_JP002 に紐付いている) を修正:
//       V_JP001_01 (限定A)  → V_JP002_01  sort_order=1
//       V_JP001_02 (限定B)  → V_JP002_02  sort_order=2
//       V_JP001_03 (限定C)  → DELETE
//       V_JP001_04 (通常)   → V_JP002_03  sort_order=3
//   - 既に 01-we-make-you.mjs が作成した V_JP002_CARAT (Carat盤) は維持。
//     sort_order を 5 → 4 に更新 (通常 の直後に並べる)。
//
// 使い方:
//   ドライラン: node scripts/seed-jp-batch-2026-04/01b-we-make-you-cleanup.mjs --dry
//   本実行    : node scripts/seed-jp-batch-2026-04/01b-we-make-you-cleanup.mjs

import { s } from './_shared.mjs'

const PID = 'P_JP002'
const DRY = process.argv.includes('--dry')

// Rename plan: old version_id → new version_id, with patch fields
// `delete: true` means drop the row entirely.
const PLAN = [
  { old: 'V_JP001_01', new: 'V_JP002_01', patch: { version_name: '限定A', tier: 'INCLUDED', sort_order: 1 } },
  { old: 'V_JP001_02', new: 'V_JP002_02', patch: { version_name: '限定B', tier: 'INCLUDED', sort_order: 2 } },
  { old: 'V_JP001_03', delete: true },
  { old: 'V_JP001_04', new: 'V_JP002_03', patch: { version_name: '通常',  tier: 'INCLUDED', sort_order: 3 } },
]

// card_master.id pattern generated by 01-we-make-you.mjs / buildMemberCards():
//   CM_<PID>_<idSuffix>[_<i>]_<memberId>
// The old rows (if any) were created under PID=P_JP002 for these IDs, so the
// CM_* id itself does not encode the version_id. We only have to update
// card_master.version_id column.

async function printCounts(label) {
  const { data: vers, error: vErr } = await s
    .from('card_versions')
    .select('version_id, version_name, tier, sort_order, product_id')
    .eq('product_id', PID)
    .order('sort_order', { ascending: true, nullsFirst: false })
    .order('version_id')
  if (vErr) throw vErr
  console.log(`\n[${label}] card_versions for ${PID}: ${vers.length}`)
  let totalCards = 0
  for (const v of vers) {
    const { count } = await s
      .from('card_master')
      .select('id', { count: 'exact', head: true })
      .eq('version_id', v.version_id)
    totalCards += count ?? 0
    console.log(`  ${v.version_id} | tier=${v.tier} | sort=${v.sort_order} | ${v.version_name} | cards=${count}`)
  }
  // Also count rows that refer to the "bad" V_JP001_* ids (which belong to
  // P_JP002 but have the wrong prefix).
  const { data: legacyVers } = await s
    .from('card_versions')
    .select('version_id, product_id, version_name, tier, sort_order')
    .in('version_id', ['V_JP001_01', 'V_JP001_02', 'V_JP001_03', 'V_JP001_04'])
  if (legacyVers?.length) {
    console.log(`  [legacy V_JP001_* present: ${legacyVers.length}]`)
    for (const v of legacyVers) {
      const { count } = await s
        .from('card_master')
        .select('id', { count: 'exact', head: true })
        .eq('version_id', v.version_id)
      console.log(`    ${v.version_id} (product=${v.product_id}) | ${v.version_name} | cards=${count}`)
    }
  }
  console.log(`  => total card_master rows (by ${PID} versions): ${totalCards}`)
  return { versionCount: vers.length, cardCount: totalCards, legacyCount: legacyVers?.length ?? 0 }
}

async function main() {
  console.log(`\n================ ${PID} We Make You — CLEANUP ${DRY ? '(DRY RUN)' : ''} ================`)

  // safety: user_cards must be 0 for PID
  const { count: ucCount } = await s
    .from('user_cards')
    .select('id', { count: 'exact', head: true })
    .eq('product_id', PID)
  console.log(`[safety] user_cards for ${PID} = ${ucCount ?? 0}`)
  if ((ucCount ?? 0) > 0 && !DRY) {
    console.error(`ABORT: user_cards has ${ucCount} rows for ${PID}; refusing to rewrite version_ids.`)
    process.exit(1)
  }

  // before snapshot
  await printCounts('BEFORE')

  let deletedCardMaster = 0
  let deletedVersions = 0
  let renamedCardMaster = 0
  let renamedVersions = 0
  let caratUpdated = 0

  // --- Step 1: DELETE V_JP001_03 (限定C) ---
  console.log(`\n[1] delete 限定C (V_JP001_03)`)
  const { count: cmCount } = await s
    .from('card_master')
    .select('id', { count: 'exact', head: true })
    .eq('version_id', 'V_JP001_03')
  console.log(`    card_master rows under V_JP001_03: ${cmCount ?? 0}`)
  if (!DRY) {
    if ((cmCount ?? 0) > 0) {
      const { error: dErr } = await s.from('card_master').delete().eq('version_id', 'V_JP001_03')
      if (dErr) throw new Error(`delete card_master V_JP001_03: ${dErr.message}`)
      deletedCardMaster += cmCount ?? 0
    }
    const { data: delV, error: dvErr } = await s
      .from('card_versions')
      .delete()
      .eq('version_id', 'V_JP001_03')
      .select('version_id')
    if (dvErr) throw new Error(`delete card_versions V_JP001_03: ${dvErr.message}`)
    deletedVersions += delV?.length ?? 0
    console.log(`    deleted card_master=${deletedCardMaster}, card_versions=${deletedVersions}`)
  } else {
    console.log(`    [dry] would delete card_master rows (${cmCount ?? 0}) and card_versions V_JP001_03`)
  }

  // --- Step 2: RENAME V_JP001_01, _02, _04 → V_JP002_01, _02, _03 ---
  // We must update card_master.version_id first to point at the NEW id, but
  // the FK (if any) prevents updating to a non-existent row. Safer approach:
  //   a) INSERT new card_versions row with V_JP002_* (copied from old row).
  //   b) UPDATE card_master.version_id to new id.
  //   c) DELETE old card_versions row (V_JP001_*).
  // That keeps FK happy throughout.
  console.log(`\n[2] rename V_JP001_* → V_JP002_*`)
  for (const p of PLAN) {
    if (p.delete) continue
    // read old row
    const { data: oldRow } = await s
      .from('card_versions')
      .select('version_id, product_id, version_name, tier, sort_order')
      .eq('version_id', p.old)
      .maybeSingle()
    if (!oldRow) {
      console.log(`    ${p.old} not found — skip`)
      continue
    }
    // count card_master to be re-linked
    const { count: rowCnt } = await s
      .from('card_master')
      .select('id', { count: 'exact', head: true })
      .eq('version_id', p.old)
    console.log(`    ${p.old} (${oldRow.version_name}) → ${p.new} (${p.patch.version_name})  cards=${rowCnt ?? 0}`)
    if (DRY) continue

    // check new id not already present
    const { data: newExisting } = await s
      .from('card_versions')
      .select('version_id')
      .eq('version_id', p.new)
      .maybeSingle()

    if (!newExisting) {
      const { error: iErr } = await s.from('card_versions').insert({
        version_id: p.new,
        product_id: PID,
        version_name: p.patch.version_name,
        tier: p.patch.tier,
        sort_order: p.patch.sort_order,
      })
      if (iErr) throw new Error(`insert ${p.new}: ${iErr.message}`)
      renamedVersions++
    } else {
      // make sure patch fields match
      const { error: uErr } = await s
        .from('card_versions')
        .update(p.patch)
        .eq('version_id', p.new)
      if (uErr) throw new Error(`update ${p.new}: ${uErr.message}`)
      console.log(`      (${p.new} already existed; patched)`)
    }

    // relink card_master rows
    if ((rowCnt ?? 0) > 0) {
      const { error: reErr } = await s
        .from('card_master')
        .update({ version_id: p.new })
        .eq('version_id', p.old)
      if (reErr) throw new Error(`relink card_master ${p.old} → ${p.new}: ${reErr.message}`)
      renamedCardMaster += rowCnt ?? 0
    }

    // delete old card_versions row
    const { error: doErr } = await s
      .from('card_versions')
      .delete()
      .eq('version_id', p.old)
    if (doErr) throw new Error(`delete old ${p.old}: ${doErr.message}`)
  }

  // --- Step 3: fix Carat盤 sort_order to 4 ---
  console.log(`\n[3] set V_JP002_CARAT sort_order = 4`)
  const { data: carat } = await s
    .from('card_versions')
    .select('version_id, sort_order, version_name')
    .eq('version_id', 'V_JP002_CARAT')
    .maybeSingle()
  if (!carat) {
    console.log(`    V_JP002_CARAT not found — skip`)
  } else {
    console.log(`    current sort_order=${carat.sort_order} → 4`)
    if (!DRY) {
      const { error: cErr } = await s
        .from('card_versions')
        .update({ sort_order: 4 })
        .eq('version_id', 'V_JP002_CARAT')
      if (cErr) throw new Error(`update V_JP002_CARAT: ${cErr.message}`)
      caratUpdated = 1
    }
  }

  // --- summary ---
  console.log(`\n[summary 01b-we-make-you-cleanup] ${DRY ? '(dry run, no writes)' : ''}`)
  console.log(`  deleted card_master rows         : ${deletedCardMaster}`)
  console.log(`  deleted card_versions rows       : ${deletedVersions}`)
  console.log(`  renamed card_versions (new rows) : ${renamedVersions}`)
  console.log(`  relinked card_master rows        : ${renamedCardMaster}`)
  console.log(`  carat sort_order updated         : ${caratUpdated}`)

  await printCounts('AFTER')
}

main().catch(e => { console.error('FATAL:', e); process.exit(1) })
